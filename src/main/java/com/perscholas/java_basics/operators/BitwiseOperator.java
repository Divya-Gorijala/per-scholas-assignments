package com.perscholas.java_basics.operators;

public class BitwiseOperator {
    public static void main(String[] args){
        //declare int variables
        int x=7;
        int y=17;
        int z;
        //x & y =1
         z= x & y;
        //Print out the z value
        System.out.println(z);
        //x | y = 23
        z =x | y;
        System.out.println(z);
    }
}
